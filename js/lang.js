let currentLang = localStorage.getItem("lang") || "ca";

function changeLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("lang", lang);
  const t = translations[lang];
  if (!t) return;

  // 1. Traducir por DATA-KEY
  // 1. Traducir por DATA-KEY
  document.querySelectorAll('[data-key]').forEach(el => {
    const key = el.getAttribute('data-key');
    if (!t[key]) return;

    // Lista de claves que deben renderizarse como HTML
    const htmlKeys = ['quisom', 'content', 'taula', 'comunicacions', 'contacteItem', 'contacteNotaFinal', 'cookieText'];

    if (htmlKeys.some(prefix => key.includes(prefix))) {
      el.innerHTML = t[key];        // ← Renderiza como HTML
    } else {
      el.innerText = t[key];        // ← Renderiza como texto normal
    }
  });

  // 2. Traducir por ID
  const translateById = (id, key, isHtml = false) => {
    const el = document.getElementById(id);
    if (el && t[key]) {
      isHtml ? el.innerHTML = t[key] : el.textContent = t[key];
    }
  };

  // 3. Lógica de programa
  const activeCard = document.querySelector('.program-card.active');
  if (activeCard) {
    const index = activeCard.id.split('-')[1];
    showDetail(parseInt(index));
  }

  // 4. NUEVO: Actualizar el estado visual de los botones
  updateActiveButton(lang);
}

// Lógica de visualización de detalles (solo para programa.html)
function showDetail(index) {
  const detailTitle = document.getElementById('detail-title');
  const detailText = document.getElementById('detail-text');
  if (!detailTitle || !detailText) return;

  const t = translations[currentLang];

  // Quitar active de todas las cards
  document.querySelectorAll('.program-card').forEach(c => c.classList.remove('active'));

  // Activar la card seleccionada
  const targetCard = document.getElementById(`card-${index}`);
  if (targetCard) targetCard.classList.add('active');

  detailTitle.innerText = t[`card${index}_Title`];

  if (index === 3) {

    detailText.innerHTML = renderWorkshops(t);
  }
  else if (index === 5) {
    detailText.innerHTML = rendercomunicacion(t);

  } else if (index === 6) {
    detailText.innerHTML = renderTaulaRodona(t);
  }
  else {
    // Resto de cards (acreditación, apertura, etc.)
    detailText.innerHTML = t[`content${index}`] || "<p>Contenido no disponible</p>";
  }
}

